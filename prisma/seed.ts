async function main() {
  console.log(`🌱 Seeding admin: ${SEED_ADMIN_EMAIL}`);

  const admin = await prisma.user.upsert({
    where: { email: SEED_ADMIN_EMAIL },
    update: { role: Role.ADMIN },
    create: {
      email: SEED_ADMIN_EMAIL,
      name: "CARIA Seed Admin",
      role: Role.ADMIN,
    },
  });

  console.log(`🌱 Seeding ${PROMPTS.length} prompts...`);

  for (const p of PROMPTS) {
    await prisma.prompt.upsert({
      where: { id: p.id },
      update: {
        title: p.title,
        text: p.text,
        stage: p.stage,
        subject: p.subject,
        skill: p.skill,
        grade: p.grade,
      },
      create: {
        id: p.id,
        title: p.title,
        text: p.text,
        stage: p.stage,
        subject: p.subject,
        skill: p.skill,
        grade: p.grade,
        createdById: admin.id,
      },
    });
  }

  console.log("✅ Seed completed.");
}

main()
  .catch((e) => {
    console.error("❌ Seed failed:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });